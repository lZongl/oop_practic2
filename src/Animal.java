public class Animal {
    private int weight; //практикую принцип ООП: Инкапсуляция
    private int speed;
    private int attak;

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getAttak() {
        return attak;
    }

    public void setAttak(int attak) {
        this.attak = attak;
    }
}
